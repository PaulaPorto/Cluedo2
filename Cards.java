/**
 * Abstract class Cards that has what the other types of cards should have
 * @authors antigonich singhsanj2 romcypaul alfredabra
 */
public abstract class Cards {

    /**
     * Gets the description of the card object
     * @return  the description of the card
     */
    public abstract String getDescription();

    /**
     * Returns the image file for the card
     * @return  the image file string
     */
    public abstract String getImageFile();
}
